# pragma once

# include "common.h"
#include "vector"
using namespace std;

class zMesh
{
private:
    vector<float> vertices; // 顶点坐标
    vector<float> normals; // 法线坐标
    vector<float> texcoords; // 纹理坐标
public:
    zMesh(/* args */);
    ~zMesh();
    void create_a_box();
};

zMesh::zMesh(/* args */)
{
    vertices.clear();
    normals.clear();
    texcoords.clear();
}

zMesh::~zMesh()
{
}

